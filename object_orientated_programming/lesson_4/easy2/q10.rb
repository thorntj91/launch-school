# The benefits of using OOP are numerous.
# The trade off of increased complexity in our code affords us the ability
# to more naturally model problems and indeed, model more complex scenarios.
# OOP provides encapsulation by having well defined responsibilities for 
# entities through their representation as object.
# Further, objects will then have well definied interfaces which allow
# them to pass messages, and interact with other objects.
# This gives us a lot of power and flexibility in designing solutions.
# OOP allows programmers to build up the complexity of the problem through
# layering the levels of abstraction. This allows us to think on different
# levels of granularity meaning problems are easier to conceptulise. 